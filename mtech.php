<h1><b> <u>POST GRADUATE PROGRAMS </u></b></h1>

<p>JIIT has been successfully running M. Tech Programs in Biotechnology,CSE,CSE with specialization in Information Security, CSE with specialization in Mobile Technology, Data Analytics, ECE with specialization in Communication Systems, ECE with specialization in Micro Electronics Systems & Embedded Technology, Information Technology & Entrepreneurship, Materials Science & Engineering and Applied and Computational Mathematics and an MBA program.
The objective of the M. Tech Programs is to prepare professionals with advanced knowledge of the respective field who can serve industry, R&D organizations and can take up an academic career, including further studies in a relevant Ph. D Program. The 2-year M. Tech Programs are spread over four semesters.
All M. Tech Programs are designed to cover core/compulsory as well as elective subjects to advance knowledge, ability and skills of the students in their chosen area. Students can take the desired electives from the set of subjects offered from time to time to enable them to cater for their interest and to specialize in a particular field. Dissertation is work spread over the last two semesters with fourth semester fully devoted to dissertation work only, this provides ample opportunity to the student to carry out intensive work on a chosen topic resulting in an innovative and research oriented output. Seminars are included in the Program to develop presentation skills in the students.</p>



